void main() {
  //Data Types
//Numbers, Strings, Booleans, Lists, Maps, Dynamic

// Strings
  String firstName = "Edivaldo";
  String secondName = "Gomes";
  print("String: $firstName $secondName");

// Integers
  int age = 25;
  print("Integer: $age");

// Doubles
  double otherAge = 26.10;
  print("Double: $otherAge");

// Booleans
  bool status = false;
  print("Boolean: $status");

// Dynamic
  dynamic fullName = "Edivaldo Gomes";
  print("Dynamic: $fullName");
}
